// Les opérateurs de comparaison 

// Supérieur à
console.log("20 est supérieur à 18", 20 > 18)

// Supérieur ou égale à
console.log("20 est supérieur ou égale à 20", 20 >= 20)
console.log("22 est supérieur ou égale à 20", 22 >= 20)

// Inférieur à
console.log("16 est inférieur à 18", 16 < 18)

// Inférieur ou égale à
console.log("18 est inférieur ou égale à 18", 18 <= 18)
console.log("16 est inférieur ou égale à 20", 16 <= 18)

// Égale à
console.log("20 est égale à 20", 20 === 20)
